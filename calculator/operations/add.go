package operations

// Add returns its argument int as additions from a & b.
func Add(a int, b int) int {
	return int(a + b)
}

// Div returns its argument int as additions from a & b.
func Div(a int, b int) int {
	return int(a / b)
}

// Sub returns its argument int as additions from a & b.
func Sub(a int, b int) int {
	return int(a - b)
}

// Mul returns its argument int as additions from a & b.
func Mul(a int, b int) int {
	return int(a * b)
}
